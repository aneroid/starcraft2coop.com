    <h1>Frequently Asked Questions</h1>
    <p>This page contains some of the more frequently asked questions regarding Co-op and the site itself.</p>
    <h2>Gameplay</h2>

    <h3>How do I get vision of Amon's units when viewing a replay?</h3>
    <p>You will first need to find a point where you get vision of a single one of Amon's units or structures. Then, click that unit and hold the "V" key, which will reveal all of Amon's units for you.</p>
    <p>Note that if the selected unit or structure dies, you will lose vision. For persistent vision, it is best to click one of Amon's structures in an area that won't be cleared (while still holding the V key) to transfer vision.</p>

    <h3>What is the biggest mistake you see people make in Co-op?</h3>
    <p>Focusing on Bonus Objectives. Bonus objectives offer a tiny amount of experience compared to a mission victory. However, many players seem to focus on completing bonus objectives and end up losing the entire mission. Missions like <a href="/missions/cradleofdeath">Cradle of Death</a> are traps for these inexperienced players.</p>
